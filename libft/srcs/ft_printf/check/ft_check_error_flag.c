#include <ft_printf.h>

int			ft_check_error_flag(t_arg *arg)
{
	if (arg->flag.zero == 1 && arg->flag.minus == 1)
		arg->flag.zero = 0;
	if (arg->flag.space == 1 && arg->flag.plus == 1)
		arg->flag.space = 0;
	return (SUCCESS);
}
