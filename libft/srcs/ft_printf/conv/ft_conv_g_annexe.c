#include <ft_printf.h>

int		ft_calcul_zero_ap(t_arg *arg)
{
	int		tmp;
	int		ret;
	int		mtp;

	tmp = ft_abs((int)(arg->arg.f));
	if (tmp > 0)
		return (0);
	ret = -1;
	mtp = 10;
	while (tmp == 0)
	{
		ret++;
		tmp = (arg->arg.f) * mtp;
		mtp *= 10;
	}
	return (ret);
}
