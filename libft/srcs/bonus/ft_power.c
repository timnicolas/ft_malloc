double		ft_power(double nb, int power)
{
	double result;

	result = nb;
	if (power <= 0)
	{
		while (power <= 0)
		{
			result /= nb;
			++power;
		}
		return (result);
	}
	while (power > 1)
	{
		result *= nb;
		--power;
	}
	return (result);
}
