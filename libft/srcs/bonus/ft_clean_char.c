#include <libft.h>

char		*ft_clean_char(char *s, char c)
{
	int		i;
	int		j;
	char	*ret;

	i = -1;
	j = 0;
	while (s[++i])
		if (s[i] == c)
			j++;
	j = i - j;
	if (!(ret = malloc(sizeof(char) * (j + 1))))
		return (NULL);
	ret[j] = '\0';
	i = -1;
	j = -1;
	while (s[++i])
		if (s[i] != c)
			ret[++j] = s[i];
	return (ret);
}
