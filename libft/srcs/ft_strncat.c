#include <libft.h>

char		*ft_strncat(char *dest, const char *src, size_t n)
{
	int		i;
	int		j;

	i = -1;
	while (dest[++i])
		;
	j = -1;
	while (src[++j] && n-- > 0)
		dest[i + j] = src[j];
	dest[i + j] = '\0';
	return (dest);
}
